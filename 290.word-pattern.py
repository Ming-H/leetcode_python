# @lc code=start
class Solution:
    def wordPattern(self, pattern: str, s: str) -> bool:
        """
        Time complexity : O(N) where N represents the number of 
        words in the s or the number of characters in the pattern

        Space complexity : O(M) where M is the number of unique 
        characters in pattern and words in s
        """
        map_index = {}
        words = s.split()
        
        if len(pattern) != len(words):
            return False
        
        for i in range(len(words)):
            c = pattern[i]
            w = words[i]

            char_key = 'char_{}'.format(c)
            char_word = 'word_{}'.format(w)
            
            if char_key not in map_index:
                map_index[char_key] = i
            
            if char_word not in map_index:
                map_index[char_word] = i 
            
            if map_index[char_key] != map_index[char_word]:
                return False
        
        return True
# @lc code=end

